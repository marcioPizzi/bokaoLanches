    public void populaModelo() {
        int ls = tabelaPedidos.getSelectedRow();
        PedidoB.setCodPedido(Integer.parseInt(tabelaPedidos.getValueAt(ls, 0).toString()));
        PedidoB.setStatusPed(retornarStatusSelecionado());
        PedidoB.setNomeMotoboy(cbMotoboy.getSelectedItem().toString());

    }

    public void preencherTabela(String SQL) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código Pedido", "endereço", "Número", "Hora Pedido", "Status", "Motoboy"};

        //String SQL = "select * from cliente where nomeCli like '%" + jtNomeCli.getText() + "%' and situacao like 'Ativo' order by endCli";
        try {
            PreparedStatement pst = Conexao.getConnection().prepareStatement(SQL);
            ResultSet rs = pst.executeQuery();
            rs.next();
            do {
                dados.add(new Object[]{rs.getInt("codCliPed"), rs.getString("endCli"), rs.getString("numeroEnd"), rs.getDate("horaPed"), rs.getString("statusPed"), rs.getString("nomeMotoboy")});
            } while (rs.next());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex, "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
            //JOptionPane.showMessageDialog(null, "Erro ao preencher Tabela", "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tabelaPedidos.setModel(modelo);
        tabelaPedidos.getColumnModel().getColumn(0).setPreferredWidth(110);
        tabelaPedidos.getColumnModel().getColumn(0).setResizable(false);
        tabelaPedidos.getColumnModel().getColumn(1).setPreferredWidth(550);
        tabelaPedidos.getColumnModel().getColumn(1).setResizable(false);
        tabelaPedidos.getColumnModel().getColumn(2).setPreferredWidth(90);
        tabelaPedidos.getColumnModel().getColumn(2).setResizable(false);
        tabelaPedidos.getColumnModel().getColumn(3).setPreferredWidth(150);
        tabelaPedidos.getColumnModel().getColumn(3).setResizable(false);
        tabelaPedidos.getColumnModel().getColumn(4).setPreferredWidth(160);
        tabelaPedidos.getColumnModel().getColumn(4).setResizable(false);
        tabelaPedidos.getColumnModel().getColumn(5).setPreferredWidth(270);
        tabelaPedidos.getColumnModel().getColumn(5).setResizable(false);
        tabelaPedidos.getTableHeader().setReorderingAllowed(false);
        tabelaPedidos.setAutoResizeMode(tabelaPedidos.AUTO_RESIZE_OFF);
        tabelaPedidos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

