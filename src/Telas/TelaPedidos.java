    private String retornarStatusSelecionado() {
        String situacao = "NULL";
        if (jRadioButtonFazendo.isSelected()) {
            situacao = "Fazendo";
        } else if (jRadioButtonPronto.isSelected()) {
            situacao = "Pronto";
        } else if (jRadioButtonEntregue.isSelected()) {
            situacao = "Entregue na mesa";
        } else if (jRadioButtonSaiuEntrega.isSelected()) {
            situacao = "Saiu Para Entrega";
        } else if (jRadioButtonFinalizado.isSelected()) {
            situacao = "Finalizado";
        }
        return situacao;
    }

