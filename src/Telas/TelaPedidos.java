    private void jButtonAlterarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarStatusActionPerformed
        int linhaSelecionada = tabelaPedidos.getSelectedRow();
        // Verificamos se existe realmente alguma linha selecionada
        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela antes de clicar no botão alterar!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            // Obtem o modelo da JTable
            tabelaPedidos.setValueAt(retornarStatusSelecionado(), linhaSelecionada, 5);
            tabelaPedidos.setValueAt(cbMotoboy.getSelectedItem().toString(), linhaSelecionada, 6);

            cbMotoboy.requestFocus();

        }
    }//GEN-LAST:event_jButtonAlterarStatusActionPerformed

