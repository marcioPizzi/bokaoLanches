    public void preencherTabela(String pesquisa) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"C√≥digo", "Nome", "Fun√ß√£o", "Data Cadastro"};

        //String SQL = "select * from funcionario where nomeFunc like '%" + pesquisa + "%' and situacaoFunc like 'Ativo' order by nomeFunc";
        try {
            PreparedStatement pst = Conexao.getConnection().prepareStatement(pesquisa);
            ResultSet rs = pst.executeQuery();
            rs.next();
            do {
                dados.add(new Object[]{rs.getInt("codFunc"), rs.getString("nomeFunc"), rs.getString("funcaoFunc"), (Corretores.ConverterParaJava(rs.getString("dtCadFunc")))});
            } while (rs.next());

        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(null, ex, "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
            JOptionPane.showMessageDialog(null, "Erro ao preencher Tabela", "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableFuncionario.setModel(modelo);
        jTableFuncionario.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableFuncionario.getColumnModel().getColumn(0).setResizable(false);
        jTableFuncionario.getColumnModel().getColumn(1).setPreferredWidth(300);
        jTableFuncionario.getColumnModel().getColumn(1).setResizable(false);
        jTableFuncionario.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableFuncionario.getColumnModel().getColumn(2).setResizable(false);
        jTableFuncionario.getColumnModel().getColumn(3).setPreferredWidth(135);
        jTableFuncionario.getColumnModel().getColumn(3).setResizable(false);
        jTableFuncionario.getTableHeader().setReorderingAllowed(false);
        jTableFuncionario.setAutoResizeMode(jTableFuncionario.AUTO_RESIZE_OFF);
        jTableFuncionario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
